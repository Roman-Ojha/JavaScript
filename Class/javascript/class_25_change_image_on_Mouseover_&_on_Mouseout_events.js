function setnewimg() {
  // this function will apply when mouse is in hover state
  document.getElementById("img1").src = "picture/Favicon 01.jpg";
  //src is the attribute of html so we are accessing src file
}
function setoldimg() {
  // this function will apply when mouse is out of img
  document.getElementById("img1").src = "picture/Submit.png";
}
